package apitest;

public class apidbname {

	  int no;
	  String name;


	  public apidbname() {}

	  public apidbname(int no, String name) {
	    super();
	    this.no = no;
	    this.name = name;
	  }


	  public int getNo() {
	    return no;
	  }
	  public void setNo(int no) {
	    this.no = no;
	  }
	  public String getName() {
	    return name;
	  }
	  public void setName(String name) {
	    this.name = name;
	  }
	}
